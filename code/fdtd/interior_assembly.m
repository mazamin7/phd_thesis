function [A,B,C] = interior_assembly( ...
    Nx,dx,dt,...
    c,gamma,nu)

%------------------------------------------------------------
% Identity
%------------------------------------------------------------

I = speye(Nx);

%------------------------------------------------------------
% Discrete Laplacian
%------------------------------------------------------------

e = ones(Nx,1);

D = spdiags([e -2*e e],[-1 0 1],Nx,Nx);

D = D/dx^2;

%------------------------------------------------------------
% Matrices
%------------------------------------------------------------

A = ...
    (1 + gamma*dt/2)*I ...
    - (nu*dt/2)*D;

B = ...
    2*I ...
    + c^2*dt^2*D;

C = ...
    -(1 - gamma*dt/2)*I ...
    - (nu*dt/2)*D;

end